public class Basics {
    public static void main(String[] args) {
        //Binary  AND operator
        System.out.println(5 & 6);

        //Binary OR operator
        System.out.println(5 | 6);

        //Binary XOR Operator
        System.out.println(5 ^ 6);

        //Binary One's Compliment
        System.out.println(~1);

        //Right shift and Left Shift
        System.out.println(0<<2);
        System.out.println(6>>1);

    }
}
